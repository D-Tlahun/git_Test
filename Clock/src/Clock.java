

public class Clock {
   private int hour;
   private int min;
   
    public int getHour() {
    	return hour;
    }
    public int  getMin() {
    	return min;
    	
    }
    public void setHour(int hours) {
    	this.hour = hours;
    	
    }
    public void setMin(int min) {
    	this.min = min;
    }
}